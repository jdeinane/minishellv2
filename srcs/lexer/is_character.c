#include "../../minishell.h"

bool	is_quote(char c)
{
	return (c == '\'' || c == '\"');
}

bool	is_operator(char c)
{
	return (c == '|' || c == '<' || c == '>');
}

bool	is_env_var(char c)
{
	return (c == '$');
}
